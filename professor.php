							<h3>Research Summary & Plan</h3>
							<hr style="margin-top:-20px">
							<p>
								나의 주요 연구 분야는 비기능적 요구사항들에 대한 소프트웨어 보험 (software assurance of non-functional requirements) 이다.
								기술 기반 사회인 요즘에는 많은 치명적인 부분들이 소프트웨어에 의하여 관리되고 있다.
								이에 대한 예로, 기찻길의 관문 관리, 그리고 우주 탐사선의 조종 시스템 등을 들 수 있다.
								이러한 시스템에서 발생하는 오류는 수십억 달러의 경제적 손실 뿐 만 아니라 심지어 인명 피해까지 일으킬 수 있다.
								대다수의 금융 기관과 전자 상거래 기업들은 소프트웨어 시스템에 의존하고 있기 때문에 소프트웨어에서의 결함 혹은 보안 취약점으로 인하여 심각한 타격을 입을 수 있다.
							</p>
							<p>
								이러한 소프트웨어의 결함 및 보안이 중요함이 알려져 있는 데에 반해 조정, 최적화, 유지 보수등의 작업들의 문제로 인하여 심각한 피해로 이어진 실제 사례들이 존재한다.
								이를 해결 하기 위해서는 소프트웨어 개발의 진행 과정의 이해를 돕기 위한 과학적 접근이 필요하다.
								나는 품질에 대한 분석을 위해서 시스템의 구성요소에 초점을 맞춘 기존의 접근 방법에 존재하는 한계를 발견하였고 이를 해결하기 위해 새로운 접근 방법을 제시 하였다.
								새로운 접근 방법은 정성적, 정량적 측정과 예측 그리고 사람에 의한 요소들을 최적화 (소프트웨어 보험에 있어서 가장 큰 문제로 알려져 있음)를 통하여 소프트웨어 품질 분석을 진행한다.
							</p>
							<p>
								해당 연구는 세 단계에 걸쳐 진행되었다.<br/><br/>
								첫 번째 단계에서는 이해관계자의 시점들을 최적화 해주는 품질 요구사항 협상 모델 중 하나인 WinWin negotiation에 초점을 맞추었다.
								이는 나의 박사과정 논문의 주제였고, 해당 주제로 1996년도 ICRE (International Conference on Requirement Engineering)에서 최고 논문 상을 수상하였고,
								당시 키노트 발표자였던 Tom DeMarco로부터 WinWin Negotiation이 향후 10년 뒤에 요구공학의 핵심 기술이 될 것이고, 청중들에게 추천을 하는 등, 많은 연구자들의 지지도 얻을 수 있었다. <br/><br/>
								두 번째 단계는 나는 TAMU에 참여하면서 진행되었다. 나는 요구사항 협상 모델을 일정하지 않은 사람 이해관계자의 관점을 포함하는 소프트웨어 구조 분석 프레임워크로 확장시켰다.
								이 연구는 소프트웨어 공학 분야에서 가장 명성 있는 협회 중 하나인 CMU 소프트웨어 공학 협회에 의해 인정 받을 수 있었고, 결과적으로 NASA의 Jet Propulsion Laboratory로부터 70,135$의 투자를 받을 수 있었다.
								 나와 나의 학생들은 Visualized conflict Resolution(VCR)이라는 Plug-and-Analyze 구조 분석 프레임 워크를 개발하였다.
								 이 기술은 NASA의 mission-critical system에 성공적으로 도입되었다. 뿐만 아니라 이 프로젝트는 다른 기관 혹은 산업체에서도 투자를 받을 수 있었다. 이에 대한 예로, 미국 해군으로부터 160,000$, 그리고 HP로부터 120,000$를 투자 받은 사례가 있다.<br/><br/>
								앞서 소개된 두 단계의 성공으로 고양되어 나는 세 번째 단계로 가장 중요한 속성 중 하나인 보안의 성능과 비용의 소프트웨어 프로세스 시뮬레이션 기술을 사용한 정량적 분석에 더 초점을 맞추었다.
								이 단계는 2년 반 전에 시작되었다. (기준년도 불명). 이 연구를 진행하면서, 나는 대부분의 소프트웨어 보험 연구자들이 보안 공격 방어에서 가장 치명적 문제 중 하나인 사람에 대한 요소를 간과한다는 사실을 깨달을 수 있었다.
								고로, 나는 융합 페트리넷 (hybrid Petri Nets) 과 동적 시스템 모델링을 이용하여 정량적으로 보안 팀과 공격자들의 동적인 행태와 그에 대한 효과를 분석하는 사람-기계 반응 모델링 및 시뮬레이션 기술의 개발을 이끌었다.
								부서 내외의 학부생들과 팀을 이루어 일하여 (Working with a team of faculty in and outside of this department) 우리는 개개발된 모델을 네트워크 보안 문제를 구체화 하는데에 사용하였고, 이는 NASA, Sandia National Lab 와 그 동료들로부터 적극적으로 수용되었다. <br/>
